/**
 * CalendarColor.kt
 *  Enum mapping human-readable colors used by Google Calendar to their string ids
 *  Mapping found here: https://lukeboyle.com/blog-posts/2016/04/google-calendar-api---color-id/
 */
enum class CalendarColor(var colorId: String) {
    Lavender("1"),
    Sage("2"),
    Grape("3"),
    Flamingo("4"),
    Banana("5"),
    Tangerine("6"),
    Peacock("7"),
    Graphite("8"),
    Blueberry("9"),
    Basil("10"),
    Tomato("11")
}
